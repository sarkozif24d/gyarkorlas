/*
* Aritnetika
* (number, number) -> number
*/

// +,-,*,/,%
console.log(2 + 4);
console.log(2 - 4);
console.log(2 * 4);
console.log(2 / 4);
console.log(3 % 7);


/*
* Összehaspmlító
* (any, any) -> boolean
*/
//==
console.log(2 == 1);
// !=
console.log(2 != 2);

// ===
console.log(1 === true);
console.log(1 !== true);
console.log('János' === 'József');

// >, <, >=, <=
console.log(3 > 5);

/*
* Logikai
* (boolean, boolean)  -> boolean
*/

// logikai És
console.log(true && true)


// logai vagy
console.log(true || false)

// negálás
console.log(!true)

/** 
 * String operátor
 */
// konkaktenálás
console.log('boldog szüli napot' + 'darkmasked2');
// -> 'boldog szüli napot darkmasked2'

// conditinal
/*
* (bool, any, any) ->
*Conditinal
*/

console.log(true ? 'elsö' : 'második');